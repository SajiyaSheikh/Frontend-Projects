  function addItemToCart(elements){
  
    var addedItems=JSON.parse(localStorage.getItem("cart")||"[]");
    console.log("before :",addedItems);
    addedItems.push(elements);
    console.log("after",addedItems);
    
    localStorage.setItem("cart",JSON.stringify(addedItems));
    alert("Item added to cart")
    window.location.reload();
  
  }